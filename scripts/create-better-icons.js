#!/usr/bin/env node

/**
 * Create proper PWA icons using ImageMagick or Node.js canvas
 * This creates real PNG icons that browsers will accept for PWA installation
 */

const fs = require("fs");
const path = require("path");
const { execSync } = require("child_process");

const publicDir = path.join(__dirname, "..", "public");

console.log("🎨 Creating PWA icons...");

// Check if ImageMagick is available
let hasImageMagick = false;
try {
	execSync("which convert", { stdio: "pipe" });
	hasImageMagick = true;
	console.log("✅ ImageMagick found");
} catch (e) {
	console.log("ℹ️  ImageMagick not found, creating simple PNG icons");
}

if (hasImageMagick) {
	try {
		// Create a simple notification bell icon using ImageMagick
		console.log("📱 Creating 192x192 icon...");
		execSync(
			`convert -size 192x192 xc:transparent \
      -fill "#5B21B6" \
      -draw "circle 96,96 96,16" \
      -fill "#8B5CF6" \
      -draw "circle 96,80 96,30" \
      -fill "white" \
      -font Helvetica -pointsize 120 \
      -gravity center \
      -annotate +0+0 "🔔" \
      ${path.join(publicDir, "icon-192x192.png")}`,
			{ stdio: "inherit" }
		);

		console.log("📱 Creating 512x512 icon...");
		execSync(
			`convert -size 512x512 xc:transparent \
      -fill "#5B21B6" \
      -draw "circle 256,256 256,16" \
      -fill "#8B5CF6" \
      -draw "circle 256,213 256,80" \
      -fill "white" \
      -font Helvetica -pointsize 320 \
      -gravity center \
      -annotate +0+0 "🔔" \
      ${path.join(publicDir, "icon-512x512.png")}`,
			{ stdio: "inherit" }
		);

		console.log("✅ Created icons with ImageMagick");
	} catch (error) {
		console.error("❌ Error creating icons with ImageMagick:", error.message);
		createSimplePNGIcons();
	}
} else {
	createSimplePNGIcons();
}

